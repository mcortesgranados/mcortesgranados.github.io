(() => {
  const catalogRoot = document.querySelector("[data-detail-catalog]");
  const summaryRoot = document.querySelector("[data-detail-summary]");
  if (!catalogRoot || !summaryRoot || !window.KAWAII_CATALOG) return;

  const priorityLabels = {
    core: "Núcleo inicial",
    growth: "Escala",
    test: "Probar con cuidado"
  };

  const parseRange = (value) => {
    const parts = String(value).match(/\d+/g)?.map(Number) || [0, 0];
    return parts.length === 1 ? [parts[0], parts[0]] : [parts[0], parts[1]];
  };

  const render = (filter = "all") => {
    let totalItems = 0;
    let onlineLow = 0;
    let onlineHigh = 0;
    let offlineLow = 0;
    let offlineHigh = 0;

    const groups = window.KAWAII_CATALOG.map((group) => {
      const rows = group.items.filter((item) => filter === "all" || item.priority === filter);
      if (!rows.length) return "";

      totalItems += rows.length;
      rows.forEach((item) => {
        const [ol, oh] = parseRange(item.online);
        const [pl, ph] = parseRange(item.offline);
        onlineLow += ol;
        onlineHigh += oh;
        offlineLow += pl;
        offlineHigh += ph;
      });

      return `
        <article class="detail-group">
          <div class="detail-group-head">
            <img src="${group.image}" alt="">
            <div>
              <span>${rows.length} líneas</span>
              <h3>${group.family}</h3>
              <p>${group.note}</p>
            </div>
          </div>
          <div class="detail-table-wrap">
            <table class="detail-table">
              <thead>
                <tr>
                  <th>Elemento</th>
                  <th>Subelementos comercializables</th>
                  <th>Descripción comercial</th>
                  <th>PVP Colombia</th>
                  <th>Costo meta</th>
                  <th>Online/mes</th>
                  <th>Presencial/mes</th>
                </tr>
              </thead>
              <tbody>
                ${rows.map((item) => `
                  <tr>
                    <td>
                      <strong>${item.name}</strong>
                      <small>${priorityLabels[item.priority]}</small>
                    </td>
                    <td>${item.subitems.map((subitem) => `<span class="subitem-pill">${subitem}</span>`).join("")}</td>
                    <td>${item.detail}</td>
                    <td>${item.pvp}</td>
                    <td>${item.landed}</td>
                    <td>${item.online}</td>
                    <td>${item.offline}</td>
                  </tr>
                `).join("")}
              </tbody>
            </table>
          </div>
        </article>
      `;
    }).join("");

    summaryRoot.innerHTML = `
      <article><strong>${totalItems}</strong><span>líneas filtradas</span></article>
      <article><strong>${onlineLow}-${onlineHigh}</strong><span>unidades online/mes estimadas</span></article>
      <article><strong>${offlineLow}-${offlineHigh}</strong><span>unidades presenciales/mes estimadas</span></article>
      <article><strong>${onlineLow + offlineLow}-${onlineHigh + offlineHigh}</strong><span>unidades totales/mes estimadas</span></article>
    `;
    catalogRoot.innerHTML = groups;
  };

  document.querySelectorAll("[data-catalog-filter]").forEach((button) => {
    button.addEventListener("click", () => {
      document.querySelectorAll("[data-catalog-filter]").forEach((item) => item.classList.remove("is-active"));
      button.classList.add("is-active");
      render(button.dataset.catalogFilter);
    });
  });

  render();
})();
